package exercise;

// BEGIN
public class Point {
    public Integer X;
    public Integer Y;

    public Point() {
    }

    public Point(Integer X, Integer Y) {
        this.X = X;
        this.Y = Y;
    }

    public void setX (Integer X) {
        this.X = X;
    }

    public Integer getX() {
        return X;
    }

    public void setY (Integer Y) {
        this.Y = Y;
    }

    public Integer getY() {
        return Y;
    }
}
// END
